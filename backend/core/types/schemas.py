# Backtesting
# =============================================================================


@dataclass(frozen=True)
class BacktestResult:
    total_return: float          # cumulative return (%)
    annual_return: float         # annualized return (%)
    max_drawdown: float          # MDD (%)
    win_rate: float              # win rate (%)
    sharpe_ratio: float
    num_trades: int
    equity_curve: pd.Series      # index=timestamp, values=equity
    trades: pd.DataFrame         # one row per trade


@runtime_checkable
class Strategy(Protocol):
    """Trading strategy — interface for backtesting.py wrapper.

    Concrete strategies subclass ``backtesting.Strategy`` directly; this
    protocol is for typing only.
    """

    def init(self) -> None: ...

    def next(self) -> None: ...


# =============================================================================
# Auto-trading (v3 placeholder — interface only, never implemented in v0.x)
# =============================================================================


@dataclass(frozen=True)
class OrderRequest:
    market: Market
    symbol: str
    side: Literal["buy", "sell"]
    quantity: float
    order_type: Literal["market", "limit"]
    limit_price: float | None = None


@dataclass(frozen=True)
class OrderResult:
    order_id: str
    status: Literal["pending", "filled", "rejected", "cancelled"]
    filled_quantity: float
    filled_price: float | None
    timestamp: pd.Timestamp


@runtime_checkable
class BrokerProtocol(Protocol):
    """Broker interface — defined now to fix the extension shape, but never
    implemented before v3 (auto-trading). Concrete brokers (KIS, Kiwoom,
    Binance Trade) will arrive in a future PDCA cycle alongside safety
    controls (per-day spend caps, dry-run, kill switch, audit log).
    """

    name: str

    def place_order(self, req: OrderRequest) -> OrderResult: ...

    def cancel_order(self, order_id: str) -> bool: ...

    def get_positions(self) -> list[Holding]: ...

    def get_balance(self, currency: str) -> float: ...


