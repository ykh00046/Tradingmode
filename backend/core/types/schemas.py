# =============================================================================
# Portfolio
# =============================================================================


@dataclass(frozen=True)
class Holding:
    """A single position in the user's portfolio.

    ``currency`` must be specified explicitly — the analysis layer will not
    guess based on market because users may hold cross-listed instruments.
    """

    market: Market
    symbol: str
    quantity: float
    avg_price: float
    currency: Literal["KRW", "USD", "USDT"]


@dataclass(frozen=True)
class Portfolio:
    holdings: list[Holding]
    base_currency: Literal["KRW", "USD"] = "KRW"


@dataclass(frozen=True)
class FxQuote:
    """Foreign-exchange snapshot used during portfolio valuation.

    Recorded so portfolio analyses are reproducible and auditable.
    """

    pair: str           # e.g. "USDT/KRW", "USD/KRW"
    rate: float
    as_of: pd.Timestamp
    source: str = "FinanceDataReader"


@dataclass(frozen=True)
class SkippedHolding:
    """A holding excluded from aggregate analysis, with the reason recorded."""

    market: Market
    symbol: str
    reason: str


@dataclass(frozen=True)
class HoldingAnalysis:
    """Per-holding analysis result.

    All monetary fields except ``current_price_local`` are expressed in the
    portfolio's ``base_currency``.
    """

    holding: Holding
    current_price_local: float
    current_price: float
    market_value: float
    cost_basis: float
    pnl: float
    pnl_pct: float
    weight: float
    fx_rate: float
    trend: TrendState
    latest_signals: list[Signal]


@dataclass(frozen=True)
class PortfolioAnalysis:
    portfolio: Portfolio
    holdings_analysis: list[HoldingAnalysis]
    total_market_value: float
    total_cost_basis: float
    total_pnl: float
    total_pnl_pct: float
    trend_summary: dict[TrendState, int]
    base_currency: Literal["KRW", "USD"]
    fx_rates: dict[str, FxQuote]
    as_of: pd.Timestamp
    skipped_holdings: list[SkippedHolding] = field(default_factory=list)


