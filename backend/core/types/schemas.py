# =============================================================================
# Signals
# =============================================================================


@dataclass(frozen=True)
class Signal:
    """A single detected trading signal.

    ``strength`` is fixed at 1.0 in v0.4. Per-kind strength formulas are deferred
    to v2 (see Design §4.3 pseudocode).
    """

    timestamp: pd.Timestamp
    kind: SignalKind
    action: SignalAction
    price: float
    strength: float = 1.0
    detail: dict = field(default_factory=dict)


