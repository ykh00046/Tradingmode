"""Domain schemas — dataclasses, enums, and protocols.

All types here are pure Python (no external dependencies beyond pandas for
DataFrame/Timestamp typing). Pydantic equivalents live in
``backend/api/schemas.py`` and are converted at the API boundary.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from enum import Enum
from typing import Literal, Protocol, TypedDict, runtime_checkable

import pandas as pd

# =============================================================================
# Enums
# =============================================================================


class Market(str, Enum):
    CRYPTO = "crypto"
    KR_STOCK = "kr_stock"


class Interval(str, Enum):
    M1 = "1m"
    M5 = "5m"
    M15 = "15m"
    H1 = "1h"
    H4 = "4h"
    D1 = "1d"


class TrendState(str, Enum):
    UPTREND = "uptrend"          # ADX > threshold + MA aligned ascending
    DOWNTREND = "downtrend"      # ADX > threshold + MA aligned descending
    SIDEWAYS = "sideways"        # ADX <= threshold or MA mixed


class SignalKind(str, Enum):
    GOLDEN_CROSS = "golden_cross"        # MA20 ↑ MA60
    DEATH_CROSS = "death_cross"          # MA20 ↓ MA60
    RSI_OVERBOUGHT = "rsi_overbought"    # RSI 70 상향 진입
    RSI_OVERSOLD = "rsi_oversold"        # RSI 30 하향 진입
    RSI_BULL_DIV = "rsi_bull_div"        # 가격 신저점 + RSI 신저점 미경신
    RSI_BEAR_DIV = "rsi_bear_div"        # 가격 신고점 + RSI 신고점 미경신
    MACD_BULL_CROSS = "macd_bull_cross"  # MACD ↑ Signal
    MACD_BEAR_CROSS = "macd_bear_cross"  # MACD ↓ Signal


class SignalAction(str, Enum):
    BUY = "buy"
    SELL = "sell"
    NEUTRAL = "neutral"


# =============================================================================
# Indicator Configuration
# =============================================================================


class IndicatorConfig(TypedDict, total=False):
    """All keys optional. Defaults applied in ``core.indicators.compute``.

    Note: separate ``ema_periods`` (5/20/60/120) chart overlay is deferred to v2.
    v0.4 uses SMA for chart overlays; EMA(12, 26) is used internally by MACD.
    """

    sma_periods: list[int]              # default [5, 20, 60, 120]
    rsi_period: int                     # default 14
    macd: tuple[int, int, int]          # default (12, 26, 9)
    bbands: tuple[int, float]           # default (20, 2.0)
    adx_length: int                     # default 14


